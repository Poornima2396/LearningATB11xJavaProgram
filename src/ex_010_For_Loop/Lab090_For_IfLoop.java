package ex_010_For_Loop;

public class Lab090_For_IfLoop {
    public static void main(String[] args) {
        for(int poornima =0; poornima<18; poornima++){ //0 to 17 , 18 times
            if(poornima>15){
                System.out.println("gift"); //prints 2 times
            }
            else {
                System.out.println("no gift");  //prints 16 times
            }
        }
    }
}
