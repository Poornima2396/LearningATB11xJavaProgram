package ex_010_For_Loop;

public class Lab093_ForLoop_Concept {
    public static void main(String[] args) {
        //for (I ; C; U)
        //you can use datatype long and float also but not advisable
        for (long i = 1l; i<10; i++) {
            System.out.println(i); //prints 1 to 9
        }
        for (float f = 2.8f; f<10; f++) {
            System.out.println("Hi float-> " + f); //prints 2.8 to 9.8

        }
    }
}
