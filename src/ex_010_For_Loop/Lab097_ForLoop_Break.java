package ex_010_For_Loop;

public class Lab097_ForLoop_Break {
    public static void main(String[] args) {
        //break is used to come out of the loop
        for(int i=0; i<50;i++){
            if(i==5){
              //  System.out.println(i); //5
                break; //it comes out of loop once it is i==5

            }
        }
    }
}
