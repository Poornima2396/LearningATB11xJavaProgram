public class Lab026_Non_Primitive_Literal {
    public static void main(String[] args) {
        //Primitive datatype - those which cannot be further broken
        //Defined by Java
        //Size defined, Max, Min
        byte b = 10; //1 Byte -> 8 Bits
        int age = 65; //4 Byte -> 32 Bits

        //Non-Primitive -> defined by users
        //No Size defined, Max, Min
        //also called Reference Data types
        //Byte size -8 and Bit size - 64
        String name = "Poornima"; //String is bunch of character
        int[] arrays_of_items = new int[10];
            //Strings,Arrays, classes, enum,etc



    }
}
