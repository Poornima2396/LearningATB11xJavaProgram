package ex_04_Operators;

public class Lab032_Logical_Operators {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(!a);

        boolean b = true;
        System.out.println(!!b);

        boolean c = true||false; //use OR gate -> 1||0 -> 1
        System.out.println(c);



    }
}
