package ex_04_Operators;

public class Lab036_EqualTo_Operator {
    public static void main(String[] args) {
        //== --> Compares the values
        System.out.println(10==10); //True
        System.out.println(10<10); //False
        System.out.println(10>10); //False
        System.out.println(10>=10); //True
    }
}
