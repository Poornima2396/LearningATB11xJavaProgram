package ex_07_Increment_Decrement_Opr;

public class Lab_062_Pre_Inc_Op3 {
    public static void main(String[] args) {
        int a = 10;
        int result = ++a;
        System.out.println(a);
        System.out.println(result);

        //Line no | a | result
        // 5 | 10 | NA
        // 6 | 11 | 11
        // 7 | 11 | 11
    }
}
